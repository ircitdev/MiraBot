def get_daily_affirmation(user_id: Optional[int] = None) -> str:
    """
    –ü–æ–ª—É—á–∞–µ—Ç –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏—é –¥–Ω—è.
    –ò—Å–ø–æ–ª—å–∑—É–µ—Ç –¥–∞—Ç—É –¥–ª—è –≤–æ—Å–ø—Ä–æ–∏–∑–≤–æ–¥–∏–º–æ—Å—Ç–∏ (–æ–¥–Ω–∞ –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏—è –Ω–∞ –¥–µ–Ω—å).

    Args:
        user_id: ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –¥–ª—è –ø–µ—Ä—Å–æ–Ω–∞–ª–∏–∑–∞—Ü–∏–∏
    """
    # –°–æ–±–∏—Ä–∞–µ–º –≤—Å–µ –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏–∏
    all_affirmations = []
    for category_affs in AFFIRMATIONS.values():
        all_affirmations.extend(category_affs)

    # –ò—Å–ø–æ–ª—å–∑—É–µ–º –¥–∞—Ç—É –∫–∞–∫ seed –¥–ª—è –≤–æ—Å–ø—Ä–æ–∏–∑–≤–æ–¥–∏–º–æ—Å—Ç–∏
    today = date.today()
    seed = today.toordinal()
    if user_id:
        seed += user_id  # –†–∞–∑–Ω—ã–µ –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏–∏ –¥–ª—è —Ä–∞–∑–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π

    rng = random.Random(seed)
    return rng.choice(all_affirmations)


def get_affirmation_by_category(category: str) -> str:
    """–ü–æ–ª—É—á–∞–µ—Ç —Å–ª—É—á–∞–π–Ω—É—é –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏—é –∏–∑ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏."""
    if category not in AFFIRMATIONS:
        category = random.choice(list(AFFIRMATIONS.keys()))

    return random.choice(AFFIRMATIONS[category])


