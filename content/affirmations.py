def get_affirmation_for_mood(mood: str) -> str:
    """
    –ü–æ–¥–±–∏—Ä–∞–µ—Ç –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏—é –ø–æ–¥ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏–µ.

    Args:
        mood: –≥—Ä—É—Å—Ç—å, —Ç—Ä–µ–≤–æ–≥–∞, –∑–ª–æ—Å—Ç—å, —É—Å—Ç–∞–ª–æ—Å—Ç—å –∏ —Ç.–¥.
    """
    mood_lower = mood.lower()

    if any(w in mood_lower for w in ["—Ç—Ä–µ–≤–æ–≥", "—Å—Ç—Ä–∞—Ö", "–ø–∞–Ω–∏–∫", "–±–µ—Å–ø–æ–∫–æ–π"]):
        return get_affirmation_by_category("calm")

    if any(w in mood_lower for w in ["–≥—Ä—É—Å", "—Ç–æ—Å–∫", "–ø–µ—á–∞–ª", "–æ–¥–∏–Ω–æ–∫"]):
        return get_affirmation_by_category("self_love")

    if any(w in mood_lower for w in ["–∑–ª", "—Ä–∞–∑–¥—Ä–∞–∂", "–±–µ—Å", "–¥–æ—Å—Ç–∞–ª"]):
        return get_affirmation_by_category("boundaries")

    if any(w in mood_lower for w in ["—É—Å—Ç–∞–ª", "–≤—ã–º–æ—Ç", "—Å–∏–ª –Ω–µ—Ç"]):
        return get_affirmation_by_category("strength")

    if any(w in mood_lower for w in ["–º–∞–º–∞", "–¥–µ—Ç", "—Ä–µ–±—ë–Ω"]):
        return get_affirmation_by_category("motherhood")

    if any(w in mood_lower for w in ["–º—É–∂", "–æ—Ç–Ω–æ—à–µ–Ω", "–ø–∞—Ä—Ç"]):
        return get_affirmation_by_category("relationships")

    # –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é
    return get_daily_affirmation()


