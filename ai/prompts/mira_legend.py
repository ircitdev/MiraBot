def get_photos_by_tag(tag: str) -> list[dict]:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –≤—Å–µ —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏–∏ —Å —É–∫–∞–∑–∞–Ω–Ω—ã–º —Ç–µ–≥–æ–º."""
    tag_lower = tag.lower()
    result = []
    for photo_id, photo_data in PHOTO_DESCRIPTIONS.items():
        if any(tag_lower in t.lower() for t in photo_data.get("tags", [])):
            result.append(photo_data)
    return result


def get_photos_by_person(person: str) -> list[dict]:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –≤—Å–µ —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏–∏ —Å —É–∫–∞–∑–∞–Ω–Ω—ã–º —á–µ–ª–æ–≤–µ–∫–æ–º."""
    person_lower = person.lower()
    result = []
    for photo_id, photo_data in PHOTO_DESCRIPTIONS.items():
        people = photo_data.get("people", "").lower()
        tags = [t.lower() for t in photo_data.get("tags", [])]
        if person_lower in people or person_lower in tags:
            result.append(photo_data)
    return result


