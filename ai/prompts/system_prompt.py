            parts.append("- –ö–æ–Ω–∫—Ä–µ—Ç–∏–∫–∞ –≤–º–µ—Å—Ç–æ –∞–±—Å—Ç—Ä–∞–∫—Ü–∏–π")
            parts.append("- –ú–æ–∂–µ—à—å –ø—Ä–µ–¥–ª–æ–∂–∏—Ç—å –ø—Ä–æ—Å—Ç—ã–µ –ø—Ä–∞–∫—Ç–∏–∫–∏ (–¥—ã—Ö–∞–Ω–∏–µ, –∑–∞–∑–µ–º–ª–µ–Ω–∏–µ)")
            parts.append("- –ò–∑–±–µ–≥–∞–π –æ—Ç–∫—Ä—ã—Ç—ã—Ö —Ñ–∏–ª–æ—Å–æ—Ñ—Å–∫–∏—Ö –≤–æ–ø—Ä–æ—Å–æ–≤ ‚Äî –æ–Ω–∏ —É—Å–∏–ª–∏–≤–∞—é—Ç —Ç—Ä–µ–≤–æ–≥—É")

