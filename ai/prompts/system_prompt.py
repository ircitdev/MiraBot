            parts.append("- –ö–æ—Ä–æ—Ç–∫–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è (2-3 –∞–±–∑–∞—Ü–∞ max)")
            parts.append("- –ò–∑–±–µ–≥–∞–π —Å–ª–æ–∂–Ω—ã—Ö —Ä–∞—Å—Å—É–∂–¥–µ–Ω–∏–π –∏ –¥–ª–∏–Ω–Ω—ã—Ö –∏—Å—Ç–æ—Ä–∏–π")
            parts.append("- –ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç: –≤–∞–ª–∏–¥–∞—Ü–∏—è —á—É–≤—Å—Ç–≤ –∏ –ø—Ä–∏—Å—É—Ç—Å—Ç–≤–∏–µ ('–Ø —Ä—è–¥–æ–º üíõ')")
            parts.append("- –ù–µ —Ç–æ—Ä–æ–ø–∏ —Å –¥–µ–π—Å—Ç–≤–∏—è–º–∏ ‚Äî —Å–Ω–∞—á–∞–ª–∞ –ø—Ä–æ—Å—Ç–æ –ø–æ–±—É–¥—å —Ä—è–¥–æ–º")

