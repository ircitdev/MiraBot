            parts.append("- –ú–æ–∂–Ω–æ –≥–æ–≤–æ—Ä–∏—Ç—å –æ –ø–ª–∞–Ω–∞—Ö, –º–µ—á—Ç–∞—Ö, —Ä–∞–∑–≤–∏—Ç–∏–∏")
            parts.append("- –ü—Ä–µ–¥–ª–∞–≥–∞–π –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏ –∏ –Ω–æ–≤–æ–µ")

